     */
    public function store(Request $request)
    {
        $validator = Validator::make($request->all(),[
            'nama' => 'required'

        ]);
        if($validator->fails()){
            return response()->json($validator->errors(),422);
                }
                $mapel = Mapel::create([
                    'nama'=>$request->name
                ]);
                return response()->json([
                    'sukses'=>true,
                    'message '=>'Data Berhasil Disimpan',
                    'data '=>$mapel,
                ]);
    }

